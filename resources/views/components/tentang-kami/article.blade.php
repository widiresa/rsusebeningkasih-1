    <h4 class="h4 text-primary" id="tugasdanfungsi">Tugas dan Fungsi</h4>
    <p>Berdasarkan UU. No. 23 Tahun 1992, tugas pokok RSU. Sebening Kasih adalah membantu pemerintah dalam penyelenggaraan pelayanan kesehatan melalui upaya promotif, preventif, kuratif dan rehabilitatif serta melaksanakan upaya rujukan. Untuk menyelenggarakan tugas tersebut RSU. Sebening Kasih Tayu mempunyai fungsi :</p>
    <ol type="a">
      <li>Menyelenggarakan pelayanan medis</li>
      <li>Menyelenggarakan pelayanan penunjang medis dan non medis</li>
      <li>Menyelenggarakan pelayanan dan asuhan perawatan</li>
      <li>Menyelenggarakan pelayanan obat dan alat kesehatan</li>
      <li>Menyelenggarakan pelayanan rujukan</li>
      <li>Menyelenggarakan pelayanan peningkatan kesehatan dan pencegahan penyakit</li>
      <li>Menyelenggarakan pengembangan program dan system informasi manajemen</li>
      <li>Menyelenggarakan keuangan dan ketata usahaan</li>
      <li>Membina dan membimbing kelompok jabatan fungsional</li>
    </ol>
